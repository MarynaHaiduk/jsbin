// Styling with JS
let s = "Random String";

s.big();                          // Big text
s.small();                        // Small text
s.bold();                         // Bold text
s.italics();                      // Italic text
s.strike();                       // Deleted text
s.fontcolor("blue");              // Font Color
s.fontsize("8em");                // Font Size
s.sub();                          // Subscript text
s.sup();                          // Superscript text
s.link("http://google.com");      // Google
